package com.gallatinsystems.user.domain;

import java.util.Set;

import javax.jdo.annotations.PersistenceCapable;
import javax.jdo.annotations.Persistent;

import com.gallatinsystems.framework.domain.BaseDomain;

/**
 * UserRole represents a role that can be assumed by a user of the application. A role captures a
 * set of permissions that define the level of authorisation for a user.
 *
 * @author emmanuel
 */
@PersistenceCapable
public class UserRole extends BaseDomain {

    private static final long serialVersionUID = 7519627602488288941L;

    @Persistent
    private String name;

    @Persistent
    private Set<Permission> permissions;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Set<Permission> getPermissions() {
        return permissions;
    }

    public void setPermissions(Set<Permission> permissions) {
        this.permissions = permissions;
    }

}
